#!/usr/bin/env node
// Parses src/NativeLoginResult.d.ts via the TypeScript compiler API (the
// engine the LSP runs on) and writes a matching Kotlin sealed interface
// + Expo Records file so the discriminated union in the d.ts stays the
// single source of truth for the JS↔native wire shape.
//
// Usage: node scripts/generate-native-types.mjs
//        (or: npm run generate-native-types)

import ts from "typescript";
import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const inputPath = resolve(repoRoot, "src/NativeLoginResult.d.ts");

const kotlinTarget = {
  outPath: "android/src/main/java/expo/modules/criiptoverify/__generated__/NativeLoginResult.kt",
  packageName: "expo.modules.criiptoverify",
};

const DISCRIMINATOR = "kind";

const program = ts.createProgram({
  rootNames: [inputPath],
  options: { target: ts.ScriptTarget.ESNext, strict: true, noEmit: true },
});
const checker = program.getTypeChecker();
const source = program.getSourceFile(inputPath);
if (!source) throw new Error(`Cannot read ${inputPath}`);

const unions = extractUnions(source);
if (unions.length === 0) {
  console.error(`No tagged unions found in ${inputPath}.`);
  process.exit(1);
}

const kotlinFile =
  [
    `// Code generated by scripts/generate-native-types.mjs from src/NativeLoginResult.d.ts.`,
    `// DO NOT EDIT — re-run \`npm run generate-native-types\` after changing the d.ts.`,
    "",
    `package ${kotlinTarget.packageName}`,
    "",
    `import expo.modules.kotlin.records.Field`,
    `import expo.modules.kotlin.records.Record`,
    "",
    ...unions.map(renderKotlin),
  ].join("\n") + "\n";

writeFileSync(resolve(repoRoot, kotlinTarget.outPath), kotlinFile);
console.log(`wrote ${kotlinTarget.outPath}`);

function extractUnions(sourceFile) {
  const out = [];
  ts.forEachChild(sourceFile, (node) => {
    if (!ts.isTypeAliasDeclaration(node)) return;
    const type = checker.getTypeAtLocation(node);
    if (!type.isUnion()) return;
    const variants = type.types.map((vt) => describeVariant(vt, node));
    requireKindDiscriminator(node.name.text, variants);
    out.push({ name: node.name.text, variants });
  });
  return out;
}

function describeVariant(variantType, node) {
  const props = checker.getPropertiesOfType(variantType).map((sym) => {
    const declaredAt = sym.valueDeclaration ?? sym.declarations?.[0] ?? node;
    const rawType = checker.getTypeOfSymbolAtLocation(sym, declaredAt);
    const propType = checker.getNonNullableType(rawType);
    const optionalModifier = (sym.flags & ts.SymbolFlags.Optional) !== 0;
    const nullableType = rawType !== propType; // type includes null and/or undefined
    return {
      name: sym.name,
      optional: optionalModifier || nullableType,
      literalValue: propType.isStringLiteral() ? propType.value : null,
    };
  });
  return { props };
}

function requireKindDiscriminator(name, variants) {
  const tags = variants.map((v) => v.props.find((p) => p.name === DISCRIMINATOR));
  const missing = tags
    .map((t, i) => (t && typeof t.literalValue === "string" ? null : i))
    .filter((i) => i !== null);
  if (missing.length > 0) {
    throw new Error(
      `${name}: every variant must have a string-literal \`${DISCRIMINATOR}\` field (variants without one: ${missing.join(", ")}).`,
    );
  }
  const values = new Set(tags.map((t) => t.literalValue));
  if (values.size !== tags.length) {
    throw new Error(`${name}: duplicate \`${DISCRIMINATOR}\` value across variants.`);
  }
}

function shapeVariants(u) {
  return u.variants.map((v) => {
    const tag = v.props.find((p) => p.name === DISCRIMINATOR).literalValue;
    return { tag, payload: v.props.filter((p) => p.name !== DISCRIMINATOR) };
  });
}

function renderKotlin(u) {
  const variants = shapeVariants(u);

  const lines = [`sealed interface ${u.name} : Record {`, `  val ${DISCRIMINATOR}: String`, ""];

  for (const v of variants) {
    const fields = [`@Field override var ${DISCRIMINATOR}: String = "${v.tag}"`];
    for (const p of v.payload) {
      const suffix = p.optional ? "?" : "";
      const init = p.optional ? "null" : `""`;
      fields.push(`@Field var ${p.name}: String${suffix} = ${init}`);
    }
    lines.push(`  class ${v.tag} : ${u.name} {`);
    fields.forEach((f, i) => {
      lines.push(`    ${f}`);
      if (i < fields.length - 1) lines.push("");
    });
    lines.push(`  }`);
    lines.push("");
  }
  lines.push(`}`);
  return lines.join("\n");
}
